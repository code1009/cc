#ifndef test_runner_hpp
#define test_runner_hpp





/////////////////////////////////////////////////////////////////////////////
//===========================================================================
typedef struct _test_case_t
{
	const char* name;
	void (*execute)(void);
}
test_case_t;





/////////////////////////////////////////////////////////////////////////////
//===========================================================================
typedef struct _test_suite_t
{
	const char* name;
	test_case_t* items;
	size_t count;
}
test_suite_t;





/////////////////////////////////////////////////////////////////////////////
//===========================================================================
void test_runner(test_suite_t* suite);





#endif // test_runner_hpp
